"""Keyword-based responses when LLM is disabled (test_zero_local)."""

import random

# (keywords list, response or callable returning response)
KEYWORD_RESPONSES = [
    (["привет", "hello", "hi", "здравствуй"], lambda name: f"Привет, {name}! Рада тебя видеть 😊 Чем могу помочь?"),
    (["как дела", "как ты", "что нового"], "Отлично! Готова помочь вам 😊 Что интересует?"),
    (
        ["помощь", "help", "что умеешь"],
        "Я умею отвечать на вопросы, поддерживать беседу и помогать! 💬\n\n"
        "В полной версии я буду уметь:\n🌤 Узнавать погоду\n🖼 Генерировать картинки\n"
        "😂 Создавать мемы\n📷 Анализировать фото\n🔍 Искать в интернете\n\nПока что просто поболтаем! 😊",
    ),
    ("погод", "К сожалению, у меня пока нет скилла для погоды 🌤 Но в полной версии я смогу узнать погоду в любом городе!"),
    (
        ["картинк", "изображени", "нарисуй", "сгенерируй"],
        "Генерация изображений — крутая функция! 🎨 В полной версии я смогу создавать картинки по вашему описанию.",
    ),
    ("мем", "Мемы — это весело! 😄 В полной версии я буду создавать мемы по вашим идеям."),
    (
        ["анекдот", "шутк"],
        lambda: random.choice([
            "Программист ставит на тумбочку два стакана: один с водой, другой пустой. С водой — если захочет пить. Пустой — если не захочет 😄",
            "— Почему программисты путают Рождество и Хэллоуин?\n— Потому что 31 OCT = 25 DEC! 🎃🎄",
            "Жена программиста просит: «Сходи в магазин, купи хлеб. И если будут яйца — возьми десяток». Программист приходит с десятью буханками хлеба 🍞",
        ]),
    ),
    (["спасибо", "благодар", "thanks"], "Пожалуйста! Всегда рада помочь 😊"),
    (["крут", "класс", "супер", "отлично", "молодец"], "Спасибо! Стараюсь быть полезной 😊"),
]


def get_keyword_response(text: str, user_name: str) -> str | None:
    """Return response for keyword match or None."""
    text_lower = text.lower()
    for keywords, response in KEYWORD_RESPONSES:
        kw_list = [keywords] if isinstance(keywords, str) else keywords
        if any(kw in text_lower for kw in kw_list):
            if callable(response):
                return response(user_name) if response.__code__.co_argcount else response()
            return response
    return None


def get_fallback_response(text: str) -> str:
    """Default reply when no keyword matches."""
    options = [
        f"Интересно! Расскажи больше про '{text}' 🤔",
        "Понимаю. А что ты думаешь об этом?",
        f"Хм, '{text}' — интересная тема! Что тебя в этом больше всего интересует?",
        "Я пока учусь понимать всё лучше. Можешь объяснить подробнее?",
    ]
    return random.choice(options)
